package percabangan;

public class Switch {
    public static void main(String[] args) {
        int jalanJalan=3;
        switch (jalanJalan){
            case 1:
                System.out.println("aku");
                break;
            case 2:
                System.out.println("mama");
                break;
            case 3:
                System.out.println("abah");
                break;
            case 4:
                System.out.println("kaka");
                break;
            default:
                System.out.println("orang lain");
                break;
                        
        }
    }
}
